package org.example.algorithm.baekjoon.해시;

import java.io.*;
import java.util.*;

public class BOJ29701_모스부호{
    public static void main(String[] args) throws IOException{
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        int N = Integer.parseInt(br.readLine());
        String[] row = br.readLine().split(" ");
        HashMap<String,String> morseMap = new HashMap<>();
        StringBuilder sb = new StringBuilder();

        morseMap.put(".-", "A");
        morseMap.put("-...", "B");
        morseMap.put("-.-.", "C");
        morseMap.put("-..", "D");
        morseMap.put(".", "E");
        morseMap.put("..-.", "F");
        morseMap.put("--.", "G");
        morseMap.put("....", "H");
        morseMap.put("..", "I");
        morseMap.put(".---", "J");
        morseMap.put("-.-", "K");
        morseMap.put(".-..", "L");
        morseMap.put("--", "M");
        morseMap.put("-.", "N");
        morseMap.put("---", "O");
        morseMap.put(".--.", "P");
        morseMap.put("--.-", "Q");
        morseMap.put(".-.", "R");
        morseMap.put("...", "S");
        morseMap.put("-", "T");
        morseMap.put("..-", "U");
        morseMap.put("...-", "V");
        morseMap.put(".--", "W");
        morseMap.put("-..-", "X");
        morseMap.put("-.--", "Y");
        morseMap.put("--..", "Z");

// 숫자
        morseMap.put(".----", "1");
        morseMap.put("..---", "2");
        morseMap.put("...--", "3");
        morseMap.put("....-", "4");
        morseMap.put(".....", "5");
        morseMap.put("-....", "6");
        morseMap.put("--...", "7");
        morseMap.put("---..", "8");
        morseMap.put("----.", "9");
        morseMap.put("-----", "0");

// 특수문자
        morseMap.put("--..--", ",");
        morseMap.put(".-.-.-", ".");
        morseMap.put("..--..", "?");
        morseMap.put("---...", ":");
        morseMap.put("-....-", "-");
        morseMap.put(".--.-.", "@");

        for(String item : row){
            sb.append(morseMap.get(item));
        }

        String answer = sb.toString();
        System.out.println(answer);

    }
}
